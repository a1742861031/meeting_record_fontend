import Vue from 'vue'
import VueRouter from 'vue-router'
import Layout from '@/layouts'
import {publicPath, routerMode} from '@/config'

Vue.use(VueRouter)
export const constantRoutes = [
  {
    path: '/login',
    component: () => import('@/views/login/index'),
    hidden: true,
  },
  {
    path: '/register',
    component: () => import('@/views/register/index'),
    hidden: true,
  },
  {
    path: '/401',
    name: '401',
    component: () => import('@/views/401'),
    hidden: true,
  },
  {
    path: '/404',
    name: '404',
    component: () => import('@/views/404'),
    hidden: true,
  },
]

export const asyncRoutes = [
  {
    path: '/',
    component: Layout,
    redirect: '/index',
    children: [
      {
        path: 'index',
        name: 'Index',
        component: () => import('@/views/index/index'),
        meta: {
          title: '首页',
          icon: 'home',
          affix: true,
        },
      },
    ],
  },
  {
    path: '/',
    component: Layout,
    redirect: '/user',
    children: [
      {
        path: 'user',
        name: 'user',
        component: () =>
          import('@/views/personnelManagement/userManagement/index'),
        meta: {
          title: '用户管理',
          icon: 'users-cog',
          affix: true,
        },
      },
    ],
  },
  {
    path: '/',
    component: Layout,
    redirect: '/edit',
    children: [
      {
        path: 'edit',
        name: 'edit',
        component: () =>
          import('@/views/editor/index'),
        meta: {
          title: '会议纪要',
          icon: 'edit',
          affix: true,
        },
      },
    ],
  },
  {
    path: '*',
    redirect: '/404',
    hidden: true,
  },
]

const router = new VueRouter({
  base: publicPath,
  mode: routerMode,
  scrollBehavior: () => ({
    y: 0,
  }),
  routes: constantRoutes,
})

export function resetRouter() {
  location.reload()
}

export default router
